const month = 3       //if there is "march" in place of 3
switch (month) {
    case 1:                            // then write "jan in place of 1"
        console.log("january");
        break;
    case 2:
        console.log("feb");
        break;
    case 3:
        console.log("march");
        break;
    case 4:
        console.log("april");
        break;
    default:
        console.log("default case match");
        
        break;
} 
